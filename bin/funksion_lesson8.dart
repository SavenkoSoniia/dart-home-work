String getCarType(int maxTime) {
  if (maxTime < 4) {
    return ('–°—É–ø–µ—Ä–∫–∞—Ä');
  } else if (maxTime >= 4 && maxTime <= 7) {
    return ('–®–≤–∏–¥–∫–∏–π —Å–ø–æ—Ä—Ç–∫–∞—Ä');
  } else {
    return ('–ó–≤–∏—á–∞–π–Ω–∞ –º–∞—à–∏–Ω–∞ ');
  }
}

